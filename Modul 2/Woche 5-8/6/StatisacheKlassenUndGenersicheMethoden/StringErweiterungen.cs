namespace StatisacheKlassenUndGenersicheMethoden
{
    public static class StringErweiterungen
    {
        public static string Verdoppeln(this string text)
        {
            return text + text;
        }

        public static int Add(this int z, int z1, int z2)
        {
            return z1 + z2;
        }
    }

}
